package com.example.custom_listview;

public class User {

    String name, realname, type, powers, country;
    int imageId;

    public User(String name, String realname, String type, String powers, String country, int imageId) {
        this.name = name;
        this.realname = realname;
        this.type = type;
        this.powers = powers;
        this.country = country;
        this.imageId = imageId;
    }
}
